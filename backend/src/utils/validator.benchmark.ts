const { validate } = require('./barcodeValidator');
const testData: string[] = require('./testData').default;
console.log('count', testData.length);

const times = [];
for (let i = 0; i < 10; i++) {
	const start = Date.now();
	for (const code in testData) {
		validate(code);
	}
	const time = Date.now() - start;
	times.push(time);
	console.log(`Run ${i + 1}: ${time}ms`);
}

console.log('Slowest:', Math.max(...times), 'ms');
console.log('Fastest:', Math.min(...times), 'ms');
console.log('Average:', times.reduce((a, b) => a + b, 0) / times.length, 'ms');
